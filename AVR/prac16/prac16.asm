.include"m32def.inc"
LDI R16,0XFF
CBI DDRC,1
SBI DDRC,0
OUT DDRC,R16

REPEAT: SBIS PINC,1
 		JMP NEXT
 		SBI PORTC,0
 		CALL DELAY
 		CBI PORTC,0 
		CALL DELAY
 		JMP REPEAT

NEXT:  SBI PORTC,0
	   CALL DELAY
	   CALL DELAY
	   CALL DELAY
	   CBI PORTC,0
	   CALL DELAY 
	   JMP REPEAT

DELAY: 	   LDI R18,0XFF
	LOOP2: LDI R17,0XFF
	LOOP1: DEC R17
		   BRNE LOOP1
		   DEC R18
		   BRNE LOOP2
		   RET
