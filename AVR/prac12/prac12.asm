 LDI R26,0X60
 LDI R27,0X00
 LDI R28,0X90
 LDI R29,0X00
 LDI R18,0X01
 MOV R17,R16

REPEAT: LD R16,X+
		CPI R16,0X00
		BREQ END
		CPI R16,0X20
		BREQ SPACE
		JMP REPEAT

SPACE: INC R17
       STS 0x91,R17
 	   JMP WORD

WORD: INC R18
	   STS 0X90, R18
	   JMP REPEAT
 
END: JMP END
