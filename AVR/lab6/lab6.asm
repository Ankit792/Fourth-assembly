.Include"m32def.inc"
LDI	R26,0x60;
LDI	R27,0x00;
LD 	R18,X;
MOV R19,R18;
MOV R20,R18;
LDI	R21,0x00;
LDI	R22,0x00;
LDI	R23,0x0A;
LDI R24,0x0A;


REP:  LD R18,X+;
	 ADC R21,R18;
	STS 0x90,R21;
	  CP R18,R19;
	BRSH MAX;
	  CP R18,R20;
	BRLO MIN;
	 JMP BK;
	    
MAX: MOV R19,R18;
	 STS 0x91,R19;
	 JMP BK;

MIN: MOV R20,R18;
	 STS 0x92,R20;
	 JMP BK;


AVG: SUB R21,R23;
	BRSH QUO;
	 ADC R23,R21;
	 JMP END;


BK: DEC R24;
	BRNE REP;

QUO: INC R22;	
	 STS 0x93,R22;
	 JMP AVG;

END: JMP END
