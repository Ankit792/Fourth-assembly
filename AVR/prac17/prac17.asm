.include "m32def.inc"

CBI DDRA,2
SBI DDRA,3
LDI R19,0XFF
OUT DDRD,R19
LDI R22,0X00

SELECT: SBIS PINA,2
        JMP OUTP
	    LDI R16,0X45
	    OUT PORTD,R16
	    SBI PORTA,3
	    CALL DELAY
	    CBI PORTA,3
	    CALL DELAY
	    JMP SELECT

OUTP: OUT PORTD,R22
	 CBI PORTA,3
	 JMP SELECT

DELAY: LDI R20,0X03
 LOP2: LDI R21,40
 LOP1: DEC R21
       BRNE LOP1
	   DEC R20
	   BRNE LOP2
       RET
